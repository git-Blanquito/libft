#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		z;
	int		i;

	z = 0;
	i = ft_strlen(s);
	while (z < i)
	{
		f(z, s);
		z++;
		s++;
	}
}
