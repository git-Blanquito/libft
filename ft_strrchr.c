#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c > 255)
		c = c - ((c / 256) * 256);
	while (*s != '\0')
	{
		s++;
		i++;
	}
	if (i != 0)
	{
		while ((*s != c) && (i != 0))
		{
			s--;
			i--;
		}
	}
	if (*s == c)
		return ((char *)s);
	return (0);
}
